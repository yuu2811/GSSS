def score_by_brackets(value, brackets, *, default_label="データなし", default_score=5):
    """汎用的なブラケット判定ユーティリティ。

    Args:
        value: 判定する値 (None の場合はデフォルト返却)
        brackets: [(上限, label, score), ...] or [(上限, label), ...]
                  上限が昇順であること。
        default_label: value が None の場合のラベル
        default_score: value が None の場合のスコア

    Returns:
        (label, score) タプル。brackets に score がなければ score=None。
    """
    if value is None:
        return default_label, default_score

    for entry in brackets:
        threshold = entry[0]
        label = entry[1]
        score = entry[2] if len(entry) > 2 else None
        if value < threshold:
            return label, score
    # フォールバック: 最後のブラケット
    last = brackets[-1]
    return last[1], last[2] if len(last) > 2 else None
